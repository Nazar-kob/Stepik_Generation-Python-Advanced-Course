from itertools import combinations

count_item = int(input())
list_item = [int(input()) for item in range(count_item)]
about_item = int(input())

for i in combinations(list_item, 2):
    if i[0] * i[1] == about_item:
        print("YES")
        break
else:
    print("NO")
