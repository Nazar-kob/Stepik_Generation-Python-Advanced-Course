# –ß–µ—Ç–≤–µ—Ä—Ç–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å: 0




def find_coord(x: list) -> dict:
    parts = {'–ü–µ—Ä–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:': 0, '–í—Ç–æ—Ä–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:': 0, '–¢—Ä–µ—Ç—å—è —á–µ—Ç–≤–µ—Ä—Ç—å:': 0, '–ß–µ—Ç–≤–µ—Ä—Ç–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:': 0}
    for i in x:
        if i[0] > 0 and i[1] > 0:
            parts['–ü–µ—Ä–≤–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:'] += 1
        elif i[0] < 0 and i[1] > 0:
            parts['–í—Ç–æ—Ä–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:'] += 1
        elif i[0] < 0 and i[1] < 0:
            parts['–¢—Ä–µ—Ç—å—è —á–µ—Ç–≤–µ—Ä—Ç—å:'] += 1
        elif i[0] > 0 and i[1] < 0:
            parts['–ß–µ—Ç–≤–µ—Ä—Ç–∞—è —á–µ—Ç–≤–µ—Ä—Ç—å:'] += 1
    return parts


def add_coordinates(amount: int) -> list:
    list_coordinates = []
    for i in range(amount):
        x, y = map(int, input().split())
        list_coordinates.append((x, y))
    return list_coordinates


def task1():
    x = add_coordinates(int(input()))
    y = find_coord(x)
    for x, y in y.items():
        print(x, y)


if __name__ == '__main__':
    task1()
